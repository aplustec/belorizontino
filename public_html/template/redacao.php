    <h1>Joaquim: um brasileiro que acendeu o pavio da esperança em nosso país</h1>
    <img src="imagens/colunistas/joaquim.jpg" width="200" height="150" class="imgleft" />
    <h2>
      Joaquim Benedito Barbosa Gomes: um mineiro de Paracatu, com 58 anos de idade, oriundo de uma família muito simples, com oito irmãos, sendo ele o primogênito. Sempre estudou em escola pública e começou a trabalhar aos 16 anos, no Correio Brasiliense. Mais tarde, formou-se em Direito e obteve o seu mestrado, ambos pela Universidade de Brasília. Oficial da Chancelaria do Ministério das Relações Exteriores, Barbosa passou a ocupar em 25 de junho de 2003 uma cadeira no Supremo Tribunal Federal. Este é o ministro Joaquim Barbosa, um ilustre brasileiro que vem mudando a cara do nosso país. Um homem íntegro, corajoso e respeitado, que tem devolvido à nossa sociedade a certeza de que a Justiça ainda respira em nosso país.
      <br />
      <br />
      Relator do processo que investiga o mensalão (dinheiro público utilizado para a compra de apoio no Congresso no governo Lula), Barbosa tem sido o personagem principal dentro do STF. Com mãos de ferro vem conduzindo um longo processo que envolve influentes políticos e empresários, até então "intocáveis" pelo poder público. A tarefa do relator não tem sido fácil. Dentro do próprio Supremo ele encontra resistências, por parte dos colegas, no que tange a busca insaciável do ministro pela investigação, pela justiça e pela condenação dos culpados.
      <br />
      <br />
      O sentimento que hoje mais aflora dentro da nossa sociedade é que o Brasil está vivenciando um período histórico e que, definitivamente, estamos caminhando para tão famosa separação do "joio do trigo". Não estamos sós! Não estamos mais à mercê da corrupção, do desvio de dinheiro público e do abuso do poder econômico e que a "quadrilha", instaurada em nosso país, terá que prestar contas aos cidadãos brasileiros. E mesmo aqueles que diziam e ainda afirmam que nada sabiam sobre o abominável esquema do mensalão, por incrível que pareça, também já estão sob a mira da Justiça.
      <br />
      <br />
      Não temos dúvidas que, pela primeira vez em nosso país, o STF está investigando, julgando e punindo, severamente, aqueles que se utilizaram do poder de forma arbitrária. O Supremo resolveu nos devolver o direito de sonhar com um país mais justo, soberano e descente. Literalmente, um país "ficha limpa"! Contudo, não nos iludamos que as mudanças acontecerão em um curto espaço de tempo e que a honestidade voltará a ser o pilar de sustentação da nossa política. Ainda há muita sujeira escondida debaixo do tapete que precisa ser descoberta. É necessário que, paralelo ao trabalho dos ministros do Supremo, o Congresso retome, em caráter de urgência, os trâmites de uma ampla reforma política que, dentre tantas mudanças substanciais, possibilite a participação popular no Conselho de Ética que julga os parlamentares.
      <br />
      <br />
      O primeiro passo foi dado! A provável condenação dos políticos e demais envolvidos no esquema acende em nós, brasileiros, a chama da esperança e da justiça. E, quem sabem, em futuro próximo, teremos uma nova safra de políticos verdadeiramente conscientes da responsabilidade que exercem e cientes de que precisam primar pela transparência, pela ética e pela moralidade.
<br />
<br />
<strong>Jornalista Jo&atilde;o Martins F. Mello<br />
</strong>joaonetojornalista@belorizontino.com.br</h2></div>
